#include <stdio.h>

int	ft_iterative_power(int nb, int power)
{
	int	i;
	int	res;

	res = nb;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	if (nb == 0)
		return (0);
	i = 1;
	while (i < power)
	{
		res *= nb;
		i++;
	}
	return (res);
}
